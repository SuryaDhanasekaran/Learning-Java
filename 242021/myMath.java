import java.util.*;

public class myMath {

  public static void main(String[] args) {
    System.out.println(Math.abs(-5.3));

    System.out.println(Math.ceil(5.3));

    System.out.println(Math.floor(5.3));

    System.out.println(Math.round(5.3));

    System.out.println(Math.pow(2, 3));

    System.out.println(Math.sqrt(25));

    System.out.println(Math.random());

    System.out.println(Math.max(5, 10));

    System.out.println(Math.min(5, 10));

    System.out.println(Math.PI);

    System.out.println(Math.E);

    System.out.println(Math.log(10));

    System.out.println(Math.log10(10));

    System.out.println(Math.exp(10));

    System.out.println(Math.sin(10));

    System.out.println(Math.cos(10));

    System.out.println(Math.tan(10));

    System.out.println(Math.toDegrees(10));

    System.out.println(Math.toRadians(10));
  }
}
