public class ROIC{
    
    private String color;

    public ROIC(String color){
        this.color = color;
    }
    

    /**
     * @return String return the color
     */
    public String getColor() {
        return color;
    }

    /**
     * @param color the color to set
     */
    public void setColor(String color) {
        this.color = color;
    }

}