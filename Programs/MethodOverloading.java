/*
 * Method overloading is a feature that allows us to have more than one method
 * with the same name, so
 * long as we use different parameters
 * 
 * It is the ability to create multiple methods of the same name with different
 * implementations.
 * 
 * Calls to an overloaded method will run a specific implementation of that
 * method.
 * 
 * Same method name
 * 
 * example:
 * 
 * public static int sum(int a ,int b)
 * {
 * return a + b;
 * }
 * public static int sum(int a ,int b,int c)
 * {
 * return a + b + c;
 * }
 * public static int sum(int a ,int b,int c,int d)
 * {
 * return a + b + c + d;
 * }
 * 
 * eg:
 * println -10 methods
 * integer,double,string etc
 * 
 * 
 * Improves code readability and reusability
 * It is easier to remember one method name insted of remembering multiplenames.
 * Acheives consistency in naming.One name for methods that are commonly used
 * for exmaple println.
 * Overloaded methods give programmers the flexiblity to call similar method
 * with different
 * types of data.
 */